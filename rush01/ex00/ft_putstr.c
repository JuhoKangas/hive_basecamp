#include "sudoku.h"

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	if (str == '\0')
	{
		str = "NULL";
	}
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
